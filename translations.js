// Current language (default: English)
let currentLanguage = 'en';

// Language management functions
const LanguageManager = {
    // Get current language
    getCurrentLanguage() {
        return currentLanguage;
    },
    
    // Set language
    setLanguage(lang) {
        currentLanguage = lang;
        localStorage.setItem('language', lang);
        this.updatePageLanguage();
    },
    
    // Get translation
    translate(key) {
        return translations[currentLanguage][key] || translations.en[key] || key;
    },
    
    // Update page language
    updatePageLanguage() {
        const html = document.documentElement;
        const body = document.body;
        
        // Set direction and language attributes
        if (currentLanguage === 'ar') {
            html.setAttribute('dir', 'rtl');
            html.setAttribute('lang', 'ar');
            body.style.fontFamily = 'var(--font-arabic)';
        } else {
            html.setAttribute('dir', 'ltr');
            html.setAttribute('lang', 'en');
            body.style.fontFamily = 'var(--font-primary)';
        }
        
        // Update all elements with data-translate attribute
        const elements = document.querySelectorAll('[data-translate]');
        elements.forEach(element => {
            const key = element.getAttribute('data-translate');
            element.textContent = this.translate(key);
        });
        
        // Update placeholder texts
        const placeholders = document.querySelectorAll('[data-translate-placeholder]');
        placeholders.forEach(element => {
            const key = element.getAttribute('data-translate-placeholder');
            element.placeholder = this.translate(key);
        });
        
        // Update select options
        const selects = document.querySelectorAll('select option[data-translate]');
        selects.forEach(option => {
            const key = option.getAttribute('data-translate');
            option.textContent = this.translate(key);
        });
        
        // Update language toggle button
        const langText = document.getElementById('langText');
        if (langText) {
            langText.textContent = currentLanguage === 'en' ? 'العربية' : 'English';
        }
    },
    
    // Initialize language from localStorage
    init() {
        const savedLanguage = localStorage.getItem('language');
        if (savedLanguage && translations[savedLanguage]) {
            currentLanguage = savedLanguage;
        }
        this.updatePageLanguage();
    }
};

// Initialize language manager when DOM is loaded
document.addEventListener('DOMContentLoaded', () => {
    LanguageManager.init();
    
    // Language toggle functionality
    const languageToggle = document.getElementById('languageToggle');
    if (languageToggle) {
        languageToggle.addEventListener('click', () => {
            const newLanguage = currentLanguage === 'en' ? 'ar' : 'en';
            LanguageManager.setLanguage(newLanguage);
        });
    }
});

// Export for use in other files
window.LanguageManager = LanguageManager;
window.translations = translations;
